<template lang="pug">
  form(@submit.prevent="save")
    .form-group
      textarea.form-input(v-model="text")
    .form-actions
      button.btn.btn-ghost(v-if="isUpdate" @click.prevent="cancel") Cancel
      button.btn-blue {{isUpdate ? 'Update' : 'Submit post'}}
</template>

<script>
export default {
  props: {
    threadId: {
      required: false
    },
    post: {
      type: Object,
      validator: obj => {
        const keyIsValid = typeof obj['.key'] === 'string'
        const textIsValid = typeof obj.text === 'string'
        const valid = keyIsValid && textIsValid
        if (!textIsValid) {
          console.error('ðŸ˜³ The post prop object must include a `text` attribute.')
        }
        if (!keyIsValid) {
          console.error('ðŸ˜³ The post prop object must include a `.key` attribute.')
        }
        return valid
      }
    }
  },
  data () {
    return {
      text: this.post ? this.post.text : ''
    }
  },
  computed: {
    isUpdate () {
      return !!this.post
    }
  },
  methods: {
    save () {
      this.persist()
        .then(post => {
          this.$emit('save', { post })
        })
    },
    cancel () {
      this.$emit('cancel')
    },
    create () {
      const post = {
        text: this.text,
        threadId: this.threadId
      }
      this.text = ''
      this.$emit('save', { post })
      this.$store.dispatch('createPost', post)
      return this.$store.dispatch('createPost', post)
    },
    update () {
      const payload = {
        id: this.post['.key'],
        text: this.text
      }
      return this.$store.dispatch('updatePost', payload)
    },
    persist () {
      return this.isUpdate ? this.update() : this.create()
    }
  }
}
</script>

